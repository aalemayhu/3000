import Foundation

enum Keybinding: String {
    case PlayOrPause = " "
    case VolumeUp = "+"
    case VolumeDown = "-"
    case Loop = "l"
    case Random = "r"
    case Next = "n"
    case Previous = "p"
    case Mute = "m"
    case Tracks = "t"
    case Esc = "\u{1B}"
}
